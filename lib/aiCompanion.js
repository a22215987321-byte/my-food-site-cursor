function pickRandom(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function generateCompanionReply(role, message, nickname) {
  const bank = TEMPLATES[role] || TEMPLATES.father;
  const matched = RULES.find((r) => r.test.test(message));
  const pool = (matched && bank[matched.key]) || bank.fallback;
  let reply = pickRandom(pool);
  if (nickname && Math.random() < 0.3) reply = `${nickname}，${reply}`;
  return reply;
}

